# ถ้าเดาอารมณ์ไม่ออก ใช้คำแนะนำกลางๆ นี้
DEFAULT_SUGGESTION = {
    "display": "❓ เดาอารมณ์ไม่ออก",
    "products": [
        "เลือกสินค้าที่กำลังไวรัลใน TikTok Shop (#TikTokMadeMeBuyIt)",
        "ดูว่าคีย์เวิร์ดนี้คนค้นเพราะอะไร แล้วจับคู่สินค้าแก้ปัญหานั้น",
    ],
}


def match_product(keyword: str) -> dict:
    """
    รับคีย์เวิร์ด -> เดาอารมณ์ -> คืนหมวดสินค้าที่ควรขาย

    คืน dict: {"mood_key", "mood_display", "products"}
    """
    kw_lower = keyword.lower()

    # นับว่าคีย์เวิร์ดตรงกับ signal ของอารมณ์ไหนมากสุด
    best_mood = None
    best_hits = 0
    for mood_key, mood_data in MEME_PRODUCT_MAP.items():
        hits = sum(1 for sig in mood_data["signals"] if sig.lower() in kw_lower)
        if hits > best_hits:
            best_hits = hits
            best_mood = mood_key

    if best_mood is None:
        return {
            "mood_key": "default",
            "mood_display": DEFAULT_SUGGESTION["display"],
            "products": DEFAULT_SUGGESTION["products"],
        }

    return {
        "mood_key": best_mood,
        "mood_display": MEME_PRODUCT_MAP[best_mood]["display"],
        "products": MEME_PRODUCT_MAP[best_mood]["products"],
    }
